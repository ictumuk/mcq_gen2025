remember = """Bloom level: Remember.
- Purpose: recall/recognize facts, terms, sequences, classifications, criteria, methods.
- Question style: identify, list, define, name.
- Avoid: inference or complex application.
- Hardness-first: pick the trickiest fact/terminology confusions first (still at recall level), avoid overly trivial examples."""

understand = """Bloom level: Understand.
- Purpose: interpret meaning, explain, summarize, do simple inference, convert representations (text ↔ chart ↔ formula).
- Question style: explain why, choose the correct description, compare/contrast, select meaning.
- Avoid: calculation-heavy or complex practice scenarios.
- Hardness-first: prioritize confusing/edge representations or common misunderstandings rather than the easiest paraphrase."""

apply = """Bloom level: Apply.
- Purpose: use concepts/principles in new situations; apply laws, formulas, procedures to solve tasks.
- Question style: practice/exercise, application scenario, calculation/decision-making, “do/solve/show how to use”.
- Prefer: realistic contexts or exercises that force learners to act/solve/produce an outcome.
- Avoid: simple definition/recognition items.
- Hardness-first: choose tougher scenarios, boundary conditions, or realistic pitfalls before easy plug-and-chug cases."""
